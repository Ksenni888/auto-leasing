
CREATE TABLE IF NOT EXISTS cars (
                        VIN VARCHAR(255) PRIMARY KEY NOT NULL,
                        brand VARCHAR(255),
                        model VARCHAR(255),
                        yearofrelease INTEGER NOT NULL,
                        cost numeric NOT NULL,
                        isAvailable BOOLEAN DEFAULT 'true');

CREATE TABLE IF NOT EXISTS clients (
                        id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        name VARCHAR(255) NOT NULL,
                        passport VARCHAR(12) UNIQUE NOT NULL,
                        telephone VARCHAR(20) UNIQUE NOT NULL);

CREATE TABLE IF NOT EXISTS contracts (
                        id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        carVin VARCHAR(255) NOT NULL REFERENCES cars (VIN) ON DELETE CASCADE,
                        clientId INTEGER NOT NULL REFERENCES clients (id) ON DELETE CASCADE,
                        period INTEGER NOT NULL,
                        initialPayment numeric NOT NULL,
                        percent numeric NOT NULL,
                        amountOfFinancing numeric NOT NULL,
                        isClosed BOOLEAN DEFAULT 'false',
                        FOREIGN KEY (carVin) REFERENCES cars(VIN),
                        FOREIGN KEY (clientId) REFERENCES clients(id)
                        );

CREATE TABLE IF NOT EXISTS payments (
                       contract_id INTEGER NOT NULL,
                       payment_id INTEGER NOT NULL,
                       amount NUMERIC(15,2) NOT NULL,
                       is_paid BOOLEAN DEFAULT false,
                       PRIMARY KEY (contract_id, payment_id)
                       );

CREATE TABLE IF NOT EXISTS users (
                       id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       username VARCHAR(50) NOT NULL UNIQUE,
                       role VARCHAR(32),
                       password VARCHAR(100) DEFAULT '{noop}123',
                       enabled BOOLEAN NOT NULL
                       );
